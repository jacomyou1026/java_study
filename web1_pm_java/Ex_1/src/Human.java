
public class Human {
	private String name;
	private int age;
	private char blood;
	private float tall;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public char getBlood() {
		return blood;
	}
	public void setBlood(char blood) {
		this.blood = blood;
	}
	public float getTall() {
		return tall;
	}
	public void setTall(float tall) {
		this.tall = tall;
	}
	
	
}
