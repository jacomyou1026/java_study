package ex1_first;

public class Ex1_Print {
	//메인메서드 : 프로그램이 실행되면 가장먼저 호출되는 영역
	public static void main( String[] args ) {
		//주석 : 컴파일시에 프로그램은 인식하지 못하는 개발자간의 
		//메모나 의사소통을 위한 코드
		System.out.println("안녕하세요"); 
		System.out.println(100);
		System.out.println(50 + 50 - 11);
		
		//""뒤에 있는 +기호는 더하는 것이 아닌 '이어 붙인다'의 의미를 가진다
		System.out.println("hi : " + 10);
		System.out.println("안녕 : " + 10 + 2 + 1);
		System.out.println("2 + 2 = " + (2 + 2));
		System.out.println(2 + 2 + " hi " + 2 + 2);
		
	}//main 
}











